        public string GetUserName()
        {
            while (true)
            {
                Write("Please enter your name: ", ConsoleColor.White);
                string name = Console.ReadLine()?.Trim();

                if (!string.IsNullOrEmpty(name))
                    return name;

                WriteLine("Name cannot be empty. Please try again.", ConsoleColor.Red);
            }
        }

